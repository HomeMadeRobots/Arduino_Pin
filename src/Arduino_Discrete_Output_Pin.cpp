/* Constructor */
Arduino_Discrete_Output_Pin::Arduino_Discrete_Output_Pin( uint8_t an_arduino_pin_id )
{
    this->my_arduino_pin_id = an_arduino_pin_id;
	pinMode(an_arduino_pin_id, OUTPUT);
}
