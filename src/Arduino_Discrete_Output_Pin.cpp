/* Discrete_Pin:i_Discrete_Output */
void Arduino_Discrete_Output_Pin::Set_Level( E_IO_LEVEL level )
{
    if( level==IO_LEVEL_LOW )
	{
		digitalWrite( this->my_arduino_pin_id, LOW );
	}
	else
	{
		digitalWrite( this->my_arduino_pin_id, HIGH );
	}
}
